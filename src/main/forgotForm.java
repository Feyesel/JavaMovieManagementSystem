    private void txtUnameFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtUnameFocusLost
        // Username field foucus lost
        if(txtUname.getText().trim().equals("")){
            txtUname.setText("          After filling username Press Enter Key   ");
            txtFname.setText("");
            txtAnswer.setText("");
            txtPass.setText("");
        }
        txtUname.setForeground(new Color(102,102,102));
    }//GEN-LAST:event_txtUnameFocusLost

    private void txtUnameKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtUnameKeyPressed
        // fill username and press Enter key
        if(evt.getKeyCode() == KeyEvent.VK_ENTER){
            String role=(String)txtCombo.getSelectedItem();
            if(role.equals("User")){
                try {
                    String sql = "SELECT * FROM `user` WHERE `uname` = ?";
                    pst = con.prepareStatement(sql);
                    pst.setString(1, txtUname.getText());
                    Rs = pst.executeQuery();

                    if(Rs.next()){
                        txtFname.setText(Rs.getString(4)+" "+Rs.getString(5));
                        l_question.setText(Rs.getString(6));
                    }else{
                        JOptionPane.showMessageDialog(null, "NO RECORD MATCHES USERNAME !!!");
                    }
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(null, ex);
                }
            }else{
                try {
                    String sql = "SELECT * FROM `admin` WHERE `uname` = ?";
                    pst = con.prepareStatement(sql);
                    pst.setString(1, txtUname.getText());
                    Rs = pst.executeQuery();

                    if(Rs.next()){
                        txtFname.setText(Rs.getString(4)+" "+Rs.getString(5));
                        l_question.setText(Rs.getString(6));
                    }else{
                        JOptionPane.showMessageDialog(null, "NO RECORD MATCHES USERNAME !!!");
                    }
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(null, ex);
                }
            }
        }
    }//GEN-LAST:event_txtUnameKeyPressed

