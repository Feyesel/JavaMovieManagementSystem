    private void btnRecoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRecoverActionPerformed
        //recover button clicked
        String answer = txtAnswer.getText();
        String role=(String)txtCombo.getSelectedItem();
        if(answer.equals("")){
            JOptionPane.showMessageDialog(null, "PLAEASE ANSWER THE SECURITY QUESTION TO RETRIVE YOUR PASSWORD !","INFORMATIVE MESSAGE",JOptionPane.INFORMATION_MESSAGE);
        }else{
            if(role.equals("User")){
                try {
                    String sql = "SELECT * FROM `user` WHERE `answer`='"+answer+"'";
                    pst = con.prepareStatement(sql);
                    Rs = pst.executeQuery(); 
                    if(Rs.next()){
                        String pass = Rs.getString(3);
                        txtPass.setText("YOUR PASSWORD IS: "+pass);
                    }else{
                        JOptionPane.showMessageDialog(null, "INCORRECT ANSWER FOR THE GIVEN QUESTION!!!!");
                    }
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(null, ex);
                }
            }else{
                try {
                    String sql = "SELECT * FROM `admin` WHERE `answer`='"+answer+"'";
                    pst = con.prepareStatement(sql);
                    Rs = pst.executeQuery(); 
                    if(Rs.next()){
                        String pass = Rs.getString(3);
                        txtPass.setText("YOUR PASSWORD IS: "+pass);
                    }else{
                        JOptionPane.showMessageDialog(null, "INCORRECT ANSWER FOR THE GIVEN QUESTION!!!!");
                    }
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(null, ex);
                }
            }
        }//FOOD SELECTED CHECK
    }//GEN-LAST:event_btnRecoverActionPerformed

